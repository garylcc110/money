字符串截取题：
$str = '学习<div>php来<a href="http://www.itcast.cn">传智</a>播客</div>。';
截取前7个字符显示出来，最终应该要这个结果：
'学习<div>php来<a href="http://www.itcast.cn">传</a></div>'
要求：1. 如果字符串中有HTML标签就略过不记数
     2. 如果截取完之后有HTML标签被截断了，那么要在最后把截断的标签再补上结束标签

